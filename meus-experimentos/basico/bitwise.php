Operadores bitwise:

& = AND
| = OU
^ = XOR (ou exclusivo)
<< = Left Shift (mover bits a esquerda)
>> = Right Shift (mover bits a direita)
~ = negação


Exemplo de Left Shift:
<?php

    print( 7 << 9 ); // 3584
    /*
    7 * 2 ** 9
        2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 = 512
    7 * 512 = 3584
    */



    echo chr(10);

    print( 39 << 4 ); // 624
    /*
    39 * 2 ** 4
        2 * 2 * 2 * 2 = 16
    39 * 16 = 624
    */

    echo chr(10);


?>



Exemplo de Right Shift:

<?php
    echo 7 >> 9; // 0 (quando o resultado é inferior a 1, colocamos o zero)
    /*
    7 / 2 ** 9;
        2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 = 512
    7 / 512 = 0,013671875
    */

    echo chr(10);

    echo 39 >> 4; // 2 (nao considerar o fracionado)
    /*
    39 / 2 ** 4
        2 * 2 * 2 * 2 = 16
    39 / 16 = 2,4375
    */

    echo chr(10);

    
?>


Exemplo de negação:
<?php
    echo ~7; // -8 

    /*
        ~x = -7 - 1
        ~x = -8
    */


    echo chr(10);

    echo ~599; // -600

    /*
        ~x = -599 - 1
        ~x = -600
    */

    echo chr(10);

    echo ~-99; // ou ~(-99) => 98

    /*
        ~x = -(-99) - 1
        ~x = +99 - 1
        ~x = 98
    */

    echo chr(10);
?>



